package day13.basic;

public class MyTest {
	void aa() {
		System.out.println("aa() 角青!");
	}
	void aa(int a) {
		System.out.println("aa(int) 角青! a : " + a);
	}
	void aa(String s) {
		System.out.println("aa(String) 角青! s : " + s);
	}
	void aa(int a, int b) {
		System.out.println("aa(int, int) 角青! a, b : " + a + "," + b);
	}
	
}
